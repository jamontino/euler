public class InputOutput {
    private Integer input;
    private Integer output;

    public InputOutput(Integer input, Integer output) {
        this.input = input;
        this.output = output;
    }

    public Integer getInput() {
        return input;
    }

    public Integer getOutput() {
        return output;
    }
}
